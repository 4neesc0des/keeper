const mongoose = require("mongoose");

// schema of user registration form
const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// document name will become users in database
module.exports = mongoose.model("user", userSchema);
